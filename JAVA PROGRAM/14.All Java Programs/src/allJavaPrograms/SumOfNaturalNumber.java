package allJavaPrograms;

public class SumOfNaturalNumber {
	
		//sum of natural numbers.
		
			public static void main(String args [])
			{
				
				int a=235;
				int sum=0;
				for(int i=1;i<=a;i++)
				{
					sum=sum+i;
				}
				System.out.println("sum of natural number of  "+  a + "   is  "   +  sum);
			}
			
	}




