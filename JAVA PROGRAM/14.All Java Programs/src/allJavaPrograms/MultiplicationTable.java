package allJavaPrograms;

public class MultiplicationTable {
		
		//Multiplication of numbers 1 to 10
		
				public static void main(String args [])
				{
					for(int i=1;i<=10;i++)
					{
						//System.out.print("Value of i=" +i );
						
						for(int j=1;j<=10;j++)
						{
								System.out.print(  j*i    +  "    ");
								
						}
						
						System.out.println();
					
					}
					
				
				
	}
				
				


}
