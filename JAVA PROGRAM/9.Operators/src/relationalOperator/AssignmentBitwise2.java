package relationalOperator;

public class AssignmentBitwise2 {
	
	public static void main(String args[])
	{
		
		//Bitwise Operator
		
		
		//And operator
		System.out.println(40&10);		
		System.out.println(9&15);
		
		//Or operator
		System.out.println(40|10);
		System.out.println(9|15);
		
	
		//Xor operator
		System.out.println(40^10);		
		System.out.println(9^15);		
		
		
	}

}
