package relationalOperator;

public class UnaryPostfix {
	public static void main(String args[])
	{
		//15 16 15 16 15 16 15 
		//postfix unary increment operator 
		
		
		int a=15;                   
		System.out.println(a);            //a=15
		
		a++;                              //a=a+1=16
		System.out.println(a);            //a=16
		
		a--;                              //a=a-1=15
		System.out.println(a);            //a=15
		
		a++;                              //a=a+1=16
		System.out.println(a);            //a=16
		
		a--;                              //a=a-1=15
		System.out.println(a);            //a=15
		
		a++;                              //a=a+1=16
		System.out.println(a);            //a=16
		
		a--;                              //a=a-1=15
		System.out.println(a);            //a=15
	
	}

}
