package relationalOperator;

public class AssignmentShiftOperator {
	
	public static void main(String args[])
	{
		//Left shift operator
		System.out.println(5<<8);      //5*2^8=1280
		System.out.println(3<<8);      //3*2^8=768
		
		
		//Right shiftOperator
		System.out.println(900>>8);      //900/2^8=3
		System.out.println(500>>8);      //500/2^8=1
		
		
		
		
		
	}

}
