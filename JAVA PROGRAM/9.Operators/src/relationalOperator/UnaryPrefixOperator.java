package relationalOperator;

public class UnaryPrefixOperator {
	
	public static void main(String args [])
	{
	       int c=10;
	       System.out.println(c);                //b=10
	       
	       ++c;                                  //b=10+1=11
	       System.out.println(c);                //b=11
	       
	       --c;                                  //b=11-1=10
	       System.out.println(c);                //b=10
	       
	       ++c;                                  //b=10+1=11
	       System.out.println(c);                //b=11
	       
	       --c;                                  //b=11-1=10
	       System.out.println(c);                //b=10
	       
	       ++c;                                  //b=10+1=11
	       System.out.println(c);                //b=11
	       
	       ++c;                                  //b=11+1=12
	       System.out.println(c);                //b=12
		
		
		
	}

}
