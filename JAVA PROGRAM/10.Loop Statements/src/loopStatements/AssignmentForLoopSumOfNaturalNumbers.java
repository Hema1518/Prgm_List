package loopStatements;

public class AssignmentForLoopSumOfNaturalNumbers {
	//sum of natural numbers.
	
		public static void main(String args [])
		{
			
			int a=20;
			int sum=0;
			for(int i=1;i<=a;i++)
			{
				sum=sum+i;
				System.out.println("sum of natural number of  "+   i  + "   is  "   +  sum);
			}
			
		}
		
}

