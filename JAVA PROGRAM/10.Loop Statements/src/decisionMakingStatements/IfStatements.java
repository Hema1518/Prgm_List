package decisionMakingStatements;

public class IfStatements {
	
		public static void main(String args[])
		{
		//verify given number is greater than 20.(with false condition)
		
		int a=15;
		System.out.println("This is my given number:-"+ a);
		
		if(a>20)         //false
		{
			System.out.println("The given number is greater than 20");
		}
		
		
		//verify given number is greater than 20.(with True condition)
		
		int b=30;
		System.out.println("This is my given number:-"+ b);
		
		if(b>20)         //True
		{
			System.out.println("The given number is greater than 20");
		}
		
	}

}

//if statements--if conition is true--->it will execute.
               //--if condition is false-->it will not display anything.